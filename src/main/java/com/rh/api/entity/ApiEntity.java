package com.rh.api.entity;

public class ApiEntity {

	private String apiId;
	private String apiName;
	private String apiAge;
	private String apiMemo;
	/**
	 * @return the apiId
	 */
	public String getApiId() {
		return apiId;
	}
	/**
	 * @param apiId the apiId to set
	 */
	public void setApiId(String apiId) {
		this.apiId = apiId;
	}
	/**
	 * @return the apiName
	 */
	public String getApiName() {
		return apiName;
	}
	/**
	 * @param apiName the apiName to set
	 */
	public void setApiName(String apiName) {
		this.apiName = apiName;
	}
	/**
	 * @return the apiAge
	 */
	public String getApiAge() {
		return apiAge;
	}
	/**
	 * @param apiAge the apiAge to set
	 */
	public void setApiAge(String apiAge) {
		this.apiAge = apiAge;
	}
	/**
	 * @return the apiMemo
	 */
	public String getApiMemo() {
		return apiMemo;
	}
	/**
	 * @param apiMemo the apiMemo to set
	 */
	public void setApiMemo(String apiMemo) {
		this.apiMemo = apiMemo;
	}
}
